# Data Types

# "Hello"   <-- This is a string
# We can pull out individual characters using square brakets and
# give the index or position of the character

print("Hello"[0])
# Programmers always start counting from 0
# Because we work with binary
# This method of pulling characters from string is called
# Subscripting


# For example to pull out the last character
print("Hello"[4])
# Secret trick : We can use negative indeces
# for example, -1 <-- Signifies last character in the string

# Just remember, "123" even if a number is inside a double quotes
# and it will be considered as string


print("123" + "345")
# Will it be concatenated or added ?
# The result we got is "123345", meaning it is a string and is
# concatenated.

# To add them, we have to add integers
# Integers = Whole numbers
# We just have to write the number without anything else to declare them
print(123 + 345)

# Large Integers
