# Python Dynamic Typing

# if we take an integer and add it to a str, we get a type error
# because python is strongly typed language

# but
a = 3
print(type(a))
print(a)

a = "Hello"
print(type(a))
print(a)


# we can change a variable's datatype but changing the content in that
# variable, this is called dynamic typing

# python is unique in this sense

# strongly typed and dynamic typed are two different things.
